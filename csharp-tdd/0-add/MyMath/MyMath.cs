using System;

namespace MyMath
{
  /// <summary>
  /// Represents mathematical operations
  /// </summary>
  public class Operations
  {
    /// <summary>
    /// Method that adds two numbers
    /// </summary>
    /// <param name="a"></param>
    /// <param name="b"></param>
    /// <returns>sum of parameters</returns>
    public static int Add(int a, int b)
    {
      return a + b;
    }
  }
}